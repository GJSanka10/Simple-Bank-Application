    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
     
      
      
      try{
          
            String fname =f_name.getText();
            String lname =l_name.getText();
            String nic =txtNic.getText();
            String address =txtAddress.getText();
            String type =cmbType.getSelectedItem().toString();         
            String acc =number.getText();
            String amount=txtDep.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con1 = DriverManager.getConnection("jdbc:mysql://localhost/bank1","root","mysql");
            PreparedStatement insert = con1.prepareStatement("insert into customer(fname,lname,nic,address,type,acc,amount)values(?,?,?,?,?,?,?)");
            insert.setString(1,fname);
            insert.setString(2,lname);
            insert.setString(3,nic);
            insert.setString(4,address);
            insert.setString(5,type);
            insert.setString(6,acc);
            insert.setString(7,amount);
            
             insert.executeUpdate();
            JOptionPane.showMessageDialog(this, "Account Createdd");
            
            f_name.setText("");
            l_name.setText("");
            txtNic.setText("");
            txtAddress.setText("");
            cmbType.setSelectedIndex(-1);
            number.setText("");
            txtDep.setText("");
      
      } catch (Exception e) {
            Logger.getLogger(Add.class.getName()).log(Level.SEVERE, "Check Enter Details.", e);
        }
      
    
    }//GEN-LAST:event_btnSaveActionPerformed

